files_to_translate = [
    r'c:\Users\gozit\Desktop\project\Sip\script.js',
    r'c:\Users\gozit\Desktop\project\Sip\index.html',
]

total_replacements = 0

for file_path in files_to_translate:
    if not os.path.exists(file_path):
        print(f"❌ Файл не найден: {file_path}")
        continue

    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()

    original = content
    file_count = 0

    # Сортируем по длине ключа (длинные сначала),
    # чтобы избежать частичных замен
    for old, new in sorted(translations.items(), key=lambda x: -len(x[0])):
        if old in content:
            count = content.count(old)
            content = content.replace(old, new)
            file_count += count

    if content != original:
        with open(file_path, 'w', encoding='utf-8') as f:
            f.write(content)
        print(f"✅ Переведено: {file_path} ({file_count} замен)")
    else:
        print(f"⏭ Без изменений: {file_path}")

    total_replacements += file_count

print(f"\n🎉 Готово! Всего замен: {total_replacements}")